CREATE TABLE meeple_collection (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  user_id INTEGER REFERENCES meeple_users(id) ON DELETE CASCADE NOT NULL,
  bgame_id INTEGER REFERENCES meeple_boardgames(id) ON DELETE CASCADE NOT NULL,
  rating INTEGER,
  num_plays INTEGER NOT NULL DEFAULT 0,
  date_created TIMESTAMPTZ NOT NULL DEFAULT now() 
);